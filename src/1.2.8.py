#!/usr/bin/env python
import pandas as pd

df = pd.read_csv("mental_health_risk_dataset.csv")

# =====================================================================
# Фильтр 1: по одному признаку – люди с высоким уровнем тревожности
# =====================================================================
high_anxiety = df[df['anxiety_score'] >= 8]
print(f"Количество людей с тревожностью ≥ 8: {len(high_anxiety)}")

# =====================================================================
# Фильтр 2: по двум признакам – высокий стресс и депрессия одновременно
# =====================================================================
high_stress_depression = df[(df['stress_level'] >= 8) & (df['depression_score'] >= 8)]
print(f"Количество людей с высоким стрессом и депрессией: {len(high_stress_depression)}")

# =====================================================================
# Фильтр 3: по нескольким признакам – разведённые женщины старше 40 лет,
#           которые спят меньше 6 часов и имеют финансовый стресс > 7
# =====================================================================
filtered = df[(df['gender'] == 'Female') &
              (df['marital_status'] == 'Divorced') &
              (df['age'] > 40) &
              (df['sleep_hours'] < 6) &
              (df['financial_stress_level'] > 7)]
print(f"Количество записей, удовлетворяющих сложному фильтру: {len(filtered)}")
